print('-' * 55)
print("\nExercício 02 Estrutura de decisão\n")
n1 = float(input("Digite a 1ª nota do aluno: "))
n2 = float(input("Digite a 2ª nota do aluno: "))
média = (n1 + n2) / 2
print("Média = ",média)
if média >= 6:
  print ("Aprovado!")
elif média >= 5:
  print("Recuperação!")
else:
  print("Reprovado!")

